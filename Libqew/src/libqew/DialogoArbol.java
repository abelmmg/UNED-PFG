    private void crearArbol() {
        //create the root node
        DefaultMutableTreeNode root = new DefaultMutableTreeNode("Root");
        //create the child nodes
        DefaultMutableTreeNode Hola1 = new DefaultMutableTreeNode("Hola1");
        DefaultMutableTreeNode Hola2 = new DefaultMutableTreeNode("Hola2");
        //add the child nodes to the root node
        root.add(Hola1);
        root.add(Hola2);
         
        //create the tree by passing in the root node
        add(arbol);
        arbol.setModel(new javax.swing.tree.DefaultTreeModel(root));
        jScrollPaneArbol.setViewportView(arbol);
        
    }

