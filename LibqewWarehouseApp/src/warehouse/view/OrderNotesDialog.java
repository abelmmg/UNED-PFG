    protected void setMovelVales(HashMap<Integer, String> orderNotesList) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        // Borra las filas  del DefaultTableModel
        model.setRowCount(0);
        
        for (Map.Entry<Integer, String> entry : orderNotesList.entrySet()) {
            Object key = entry.getKey();
            Object val = entry.getValue();
            
            model.addRow(new Object[]{ key, val });
        }
    }
